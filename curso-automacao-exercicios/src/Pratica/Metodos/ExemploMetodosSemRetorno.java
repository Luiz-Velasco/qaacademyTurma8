package Pratica.Metodos;

public class ExemploMetodosSemRetorno {
      /**
     * @param args
     */
    public static void main(String[] args) {
        metodoUm();
        metodoDois();
        metodoTres();
        metodoQuatro();
        metodoCinco();
        
      }
      private static void metodoUm() {
        System.out.println("MetodoUm");
    }
    private static void metodoDois() {
        System.out.println("MetodoDois");   
    }

    private static void metodoTres() {
        System.out.println("MetodoTres");
    }

    private static void metodoQuatro() {
        System.out.println("MetodoQuatro");
    }


    private static void metodoCinco() {
        System.out.println("MetodoCinco");  
    }
   
}
